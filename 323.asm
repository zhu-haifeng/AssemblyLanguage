CALF MACRO
	PUSH DX
	MOV DL,0AH
	MOV AH,2
	INT 21H
	MOV DL,13
	MOV AH,2
	INT 21H;
	POP DX
ENDM

DATAS SEGMENT
    NUMBER DW 15,30,20
    INPUT DW 0
    K DB 16
DATAS ENDS

STACKS SEGMENT
	DW 20 DUP(?)
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    ;此处输入代码段代码
    XOR BX,0
    MOV CX,3H
AGAIN:
	PUSH CX
    MOV K,2
    CALL TRANSTOK
    CALF
    MOV K,16
    CALL TRANSTOK
    CALF
    CALF

    ADD BX,2
    POP CX
    LOOP AGAIN
    
    MOV AH,4CH
    INT 21H
TRANSTOK PROC
	MOV AX,NUMBER[BX]
	XOR CX,CX
CLASSIFY:
	
	DIV K
	ADD AH,30H
	CMP AH,3AH
	JL	PUS
	ADD AH,7H
PUS:
	PUSH AX
	INC CH
	XOR AH,AH	;置零
	CMP AL,0
	JNZ CLASSIFY
DISPLAY:
	MOV CL,8
	POP DX
	SHR DX,CL
	MOV AH,02H
	INT 21H
	DEC CH
	JNZ DISPLAY
	
	RET
TRANSTOK ENDP
CODES ENDS
    END START

