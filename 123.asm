INPUTMUM MACRO
	LEA DX,BUFF
	MOV AH,OAH
	INT 21H
	XOR BX,BX
	XOR CX,CX
	XOR DX,DX
	MOV CL,CNT
ADDTOSUM:
	SHL DX,1	;原数*2
	MOV AX,DX	;AX=原数*2
	SHL DX,2	;DX=原数*8
	ADD DX,AX	;DX=原数*10
	XOR AX,AX
	MOV AL,STR[BX]
	SUB AL,30H
	ADD DX,AX
	INC BX
	LOOP ADDTOSUM
	MOV INPUT,DX
	ENDM
	
ENDM

DISIN K
	CALL DIS&%K
	
DATAS SEGMENT
    BUFF DB 6
    CNT DB ?
    STR DB 10 DUP(?)
    INPUT DW 0
    K DB 2
DATAS ENDS

STACKS SEGMENT
    ;此处输入堆栈段代码
STACKS ENDS

CODES SEGMENT
    ASSUME CS:CODES,DS:DATAS,SS:STACKS
START:
    MOV AX,DATAS
    MOV DS,AX
    ;此处输入代码段代码
    INPUTNUM
    DISIN K
    MOV AH,4CH
    INT 21H
DIS2 PROC
	MOV CX,10H
	MOV BX,INPUT
REPEAT:
	SHR BX,1
	MOV AL,BL
	AND AL,01H
	MOV DL,AL
	ADD DL,30H
	MOV AH,02H
	INT 21H
	LOOP REPEAT

DIS
CODES ENDS
    END START
